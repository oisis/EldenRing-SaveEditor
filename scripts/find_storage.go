package main

import (
	"bytes"
	"fmt"
	"os"
)

func main() {
	data, err := os.ReadFile("tmp/save/ER0000.sl2")
	if err != nil {
		fmt.Println("Error reading file:", err)
		return
	}

	// Full 192-byte MagicPattern from Python editor
	pattern := []byte{
		0x00, 0xFF, 0xFF, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0xFF, 0xFF, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0xFF, 0xFF, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0xFF, 0xFF, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
	}

	slotStart := 0x310
	slotSize := 0x280000
	slotData := data[slotStart : slotStart+slotSize]

	fmt.Printf("Searching for 192-byte MagicPattern in Slot 0...\n")
	
	curr := 0
	for {
		idx := bytes.Index(slotData[curr:], pattern)
		if idx == -1 {
			break
		}
		absPos := curr + idx
		fmt.Printf("Found pattern at relative offset: 0x%X (Absolute: 0x%X)\n", absPos, slotStart+absPos)
		curr += idx + 1
	}
}
